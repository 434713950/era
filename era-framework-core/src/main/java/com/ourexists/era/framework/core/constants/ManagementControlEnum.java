package com.ourexists.era.framework.core.constants;

/**
 * 管理权
 */
public enum ManagementControlEnum {

    ALONE(0, "独立"),
    SERVER(1, "服务商");

    private final Integer code;

    private final String desc;

    ManagementControlEnum(Integer code, String desc) {
        this.code = code;
        this.desc = desc;
    }

    public Integer getCode() {
        return code;
    }

    public String getDesc() {
        return desc;
    }
}
